  Future<void> _loadNextReceiptPreview() async {
    try {
      final counterDoc = await FirebaseFirestore.instance
          .doc('counters/shipmentCounter')
          .get();
      int currentValue = 0;
      if (counterDoc.exists) {
        currentValue = (counterDoc.data()?['shipmentCounter'] as int?) ?? 0;
      }
      final nextValue = currentValue + 1;
      safeSetState(() {
        _model.nextReceiptNumber = '00$nextValue';
      });
    } catch (e) {
      debugPrint('Error loading receipt preview: $e');
    }
